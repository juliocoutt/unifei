'''Faça um programa que leia um nome e converta todos os caracteres para maiúscula e, em
seguida, converta todos os caracteres para minúscula'''

#ENTRADA 
#recebendo o nome 

nome = input("Qual seu nome? \n") #recebendo a entrada 

print("Maiusculo:",nome.upper()) #convertendo usado a função para Maiusculo
print("Minusculo:",nome.lower()) #convrtendo em minusculo 