'''
Faça um programa em Python que calcule a área do triângulo e apresente o
resultado.
'''

# Entradas
base = float(input("Digite a base do triângulo: ")) 
altura = float(input("Digite a altura do triângulo: "))
# Processamento 
area = (base * altura) / 2
# Saída
print("A área do triângulo é: ", area)
