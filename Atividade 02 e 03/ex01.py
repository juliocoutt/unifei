#Faça um programa em Python que receba três números e calcule a soma deles

#Entradas
n1 = input("Digite o primeiro numero: ")
n2 = input("Digite o segundo numero: ")
n3 = input("Digite o terceiro numero: ")

#Processamento
n1=int(n1)
n2=int(n2)
n3=int(n3)
soma = n1+n2+n3

#Saída
print("A soma dos numeros eh: ",soma)