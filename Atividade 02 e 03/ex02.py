'''
Faça um programa em Python que leia dois números inteiros e imprima a
média aritmética entre esses dois valores.
'''
# Entradas
n1 = input("Digite o primeiro numero: ")
n2 = input("Digite o segundo numero: ")
#Processamento
n1=int(n1)
n2=int(n2)

media = (n1+n2)/2

# Saída
print("A media aritmetica entre os dois numeros é: ",media)